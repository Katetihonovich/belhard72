# Сделать калькулятор: у пользователя
# спрашивается число, потом действие и второе
# число

a, b, c = int(input("enter number:")), input("enter operation:"), int(input("enter second number:"))
if b == "+":
    print(a+c)
if b == "-":
    print(a-c)
if b == "*":
    print(a*c)
if b == "/":
    print(a/c)













